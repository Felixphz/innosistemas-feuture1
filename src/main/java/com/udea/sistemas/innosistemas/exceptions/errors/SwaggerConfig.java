package com.udea.sistemas.innosistemas.exceptions.errors;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Contact;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.License;
import io.swagger.v3.oas.models.servers.Server;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.List;

@Configuration
public class SwaggerConfig {

    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info()
                        .title("Innosistemas API")
                        .version("1.0")
                        .description("API for managing courses, projects, teams and users")
                        .contact(new Contact()
                                .name("Innosistemas Team")
                                .email("support@innosistemas.com")
                                .url("https://innosistemas.udea.edu.co"))
                        .license(new License()
                                .name("Apache 2.0")
                                .url("https://www.apache.org/licenses/LICENSE-2.0.html")))
                .servers(List.of(
                        new Server()
                                .url("http://localhost:8080")
                                .description("Development server"),
                        new Server()
                                .url("https://api.innosistemas.udea.edu.co")
                                .description("Production server")
                ));
    }
}